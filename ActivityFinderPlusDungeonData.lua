function ACTIVITY_FINDER_PLUS.BuildDungeonIndices()
    local normalIndex = {}
    local vetIndex = {}
    local dungeonData = ACTIVITY_FINDER_PLUS.DungeonData

    for npc = 1, 3 do
        for index, dungeon in ipairs(dungeonData[npc]) do
            dungeon.npc = npc
            dungeon.index = index
            if dungeon.normal and dungeon.normal.id then
                normalIndex[dungeon.normal.id] = dungeon
            end
            if dungeon.vet and dungeon.vet.id then
                vetIndex[dungeon.vet.id] = dungeon
            end
        end
    end

    ACTIVITY_FINDER_PLUS.FinderNormalIndex = normalIndex
    ACTIVITY_FINDER_PLUS.FinderVeteranIndex = vetIndex
end

function ACTIVITY_FINDER_PLUS.GetDungeonEntry(activityId, useVeteran)
    if not activityId then
        return nil
    end
    local lookup = useVeteran and ACTIVITY_FINDER_PLUS.FinderVeteranIndex or ACTIVITY_FINDER_PLUS.FinderNormalIndex
    return lookup and lookup[activityId]
end
